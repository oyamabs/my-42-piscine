char	*ft_strcpy(char *dest, char *src)
{
	char	*p_dest;

	p_dest = dest;
	while (*src != '\0')
	{
		*dest = *src;
		dest++;
		src++;
	}
	*dest = '\0';
	return (p_dest);
}
